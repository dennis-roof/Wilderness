terrain_settings = {
    -- x map size, number of characters
    x_size = 144,
    
    -- y map size, number of characters
    y_size = 30,
    
    -- number of terrain generation cycles
    -- more cycles create a more organic looking world
    -- however, too many cycles also create a chaotic world layout
    cycles = 5,
    
    -- persistence ratio
    -- a smaller number generates more height differences, with more mountains and seas
    -- a larger number generates less height differences, with reduced mountains and seas
    persistence = 1.1,
    
    -- scale of the world
    -- a smaller number means a more zoomed in world map with larger features
    -- a larger number means for more zoomed out world map with smaller features
    scale = 0.004
}

